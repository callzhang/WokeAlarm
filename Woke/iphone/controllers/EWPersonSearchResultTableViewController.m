#pragma mark - Table view data source

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.searchResults.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"personCellIdentifier" forIndexPath:indexPath];
    
    EWPerson *person = _searchResults[indexPath.row];
    cell.textLabel.text = person.name;
    cell.detailTextLabel.text = person.distanceString;
    
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath{
    EWPerson *person = _searchResults[indexPath.row];
    DDLogInfo(@"Selected %@", person.name);
}


