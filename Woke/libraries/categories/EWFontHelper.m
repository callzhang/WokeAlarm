#import "EWFontHelper.h"

UIFont *EWRegularFontWithSize(CGFloat size) {
    return [UIFont fontWithName:@"Lato-Regular" size:size];
}

UIFont *EWLightFontWithSize(CGFloat size) {
    return [UIFont fontWithName:@"Lato-Light" size:size];
}

UIFont *EWThinFontWithSize(CGFloat size) {
    return [UIFont fontWithName:@"Lato-Thin" size:size];
}

UIFont *EWHairlineFontWithSize(CGFloat size) {
    return [UIFont fontWithName:@"Lato-Hairline" size:size];
}
